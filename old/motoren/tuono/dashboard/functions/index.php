        <div class="col-12 mt-5 mb-5">
          <h1>Dashboard Functions</h1>
          <p class="lead">This page explains all of the known functions of the dashboard on both Aprilia RSV first generation models and Aprilia Tuono first generation models</p>
        </div>
      </div>
      <div class="row">
        <div class="col-12 col-md-6 mb-4">
          <h4>Resetting the service light</h4>
          <p>Removing the spanner icon from the dashboard. This indicates Aprilia's defined maintenance intervals. It does <b>not</b> indicate any real mechanical problems.</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>LAP</code> and <code>R</code> simultaneous for 5 seconds</li>
          </ul>
        </div>

        <div class="col-12 col-md-6 mb-4">
          <h4>Switching speedometer unit of measurement</h4>
          <p>Changing between Kilometers per hour(KM/H) or Miles per hour(MPH).</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>A</code> for 5 seconds until speedometer on the LCD-screen blinks</li>
            <li>Press <code>B</code> to change unit of measurement</li>
            <li>Press <code>A</code> for 5 seconds to confirm choice </li>
          </ul>
        </div>

        <div class="col-12 col-md-6 mb-4">
          <h4>Switching temperature unit of measurement</h4>
          <p>Changing between Fahrenheit or Celsius.</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>D</code> 5 times</li>
            <li>Press <code>LAP</code> to change unit</li>
          </ul>
        </div>

        <div id="reset-odometer" class="col-12 col-md-6 mb-4">
          <h4>Reset odometer</h4>
          <p>Resetting the trip-odometer.</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>R</code> for 2 seconds</li>
          </ul>
        </div>

        <div class="col-12 col-md-6 mb-4">
          <h4>View V Max. (Velocity Maximum)</h4>
          <p>Uses trip-odometer #1 (the odometer displayed during instantaneous speed).</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>B</code> for 2 seconds</li>
          </ul>
        </div>

        <div class="col-12 col-md-6 mb-4">
          <h4>Reset V Max. (Velocity Maximum)</h4>
          <p>Uses trip-odometer #1 (the odometer displayed during instantaneous speed).</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>B</code> for 2 seconds</li>
            <li>Press <code>R</code> for 2 seconds</li>
          </ul>
        </div>

        <div id="view-avs" class="col-12 col-md-6 mb-4">
          <h4>View AVS (Average Speed)</h4>
          <p>Uses trip-odometer #2. Display AVS with trip-odometer #2.</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>B</code> for 2 seconds to display V Max. then press <code>B</code> again to view AVS.</li>
          </ul>
        </div>

        <div id="reset-avs" class="col-12 col-md-6 mb-4">
          <h4>Reset AVS and trip-odometer #2</h4>
          <p>Uses trip-odometer #2. Display AVS with trip-odometer #2.</p>
          <ul>
            <li>Complete steps in <a href="#view-avs">View AVS</a></li>
            <li>While displaying AVS, press <code>R</code> for 2 seconds</li>
          </ul>
        </div>

        <div id="set-redline" class="col-12 col-md-6 mb-4">
          <h4>Set redline threshold</h4>
          <p>Can only be set with ignition on, but the engine off. Goes in steps of 500 RPM</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>C</code>, release and press again until desired RPM is reached.</li>
            <li>Hold <code>C</code> for quicker and bigger steps.</li>
          </ul>
        </div>

        <div id="battery-voltage" class="col-12 col-md-6 mb-4">
          <h4>View battery Voltage</h4>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>D</code> once</li>
          </ul>
        </div>

        <div id="set-clock-time" class="col-12 col-md-6 mb-4">
          <h4>Set clock time</h4>
          <p>Configure the Time of Day clock.</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>D</code> twice</li>
            <li>Press <code>LAP</code> until desired hour is reached</li>
            <li>Press <code>D</code> to confirm hour</li>
            <li>Press <code>D</code> again to go to minutes</li>
            <li>Press <code>LAP</code> until desired minute is reached</li>
            <li>Press <code>D</code> to confirm</li>
          </ul>
        </div>

        <div id="set-clock-time" class="col-12 col-md-6 mb-4">
          <h4>Chronometer (lap timer)</h4>
          <p>How to use the lap timer function on the dashboard. V max., AVS and trip-odometer are not used during the use of the lap timing function.</p>
          <ul>
            <li>Ignition <code>ON</code></li>
            <li>Press <code>LAP</code> and release immediately and within a second press D to enter chromometer mode</li>
            <li>Press <code>LAP</code> and release immediately to start timing</li>
            <li>Press <code>LAP</code> to store the current time</li>
            <li>Press <code>B</code> to view stored lap times</li>
            <li>Press <code>LAP</code> to cycle trough stored lap times</li>
            <li>Press <code>A</code> and <code>LAP</code> for 2 seconds to clear the lap timer memory</li>
            <li>Press <code>LAP</code> and <code>D</code> to leave chromometer mode</li>
          </ul>
        </div>

